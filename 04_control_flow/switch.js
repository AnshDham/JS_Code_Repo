// We can declare some variable(key) and assign it a value either a Number or a String,
// Then using Switch-Case Statements we can easily compare our key with multiple values, 
// and whatsoever value perfectly matches with our key, the code inside it will get executed. 

// ++++++++++++++++++++++++++++++++++++++++++++++++

// Interview regarding important Concept:

// ++++++++++++++++++++++++++++++++++++++++++++++++

// if we don't write "break" keyword after every check, 
// then after once one case matches, it will not check values further and all the code get executed after it, "EXCEPT DEFAULT".

// ++++++++++++++++++++++++++++++++++++++++++++++++

const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}

